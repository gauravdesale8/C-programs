/* Working with numbers 2 */
#include <stdio.h>
#include <math.h>
#include <stdlib.h>
int main()
{
	printf("%f\n", pow(4,4)); /* 4 is multiplied 4 times */
	printf("%f\n", sqrt(64)); /* sqrt gives squareroot of the number */
	printf("%f\n", ceil(45.567)); /* ceil gives next highest number */
	printf("%f\n", floor(45.567)); /* floor gives previous highest number */
	return 0;
}
